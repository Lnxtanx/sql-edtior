// DBML Generator
// =============================================================================
export function generateDBML(schema: ParsedSchema): string {
  const lines: string[] = [];

  lines.push('// DBML Schema');
  lines.push('// Generated by Schema Weaver');
  lines.push('// View at dbdiagram.io');
  lines.push('');

  // Generate enums
  for (const [enumName, values] of schema.enums.entries()) {
    lines.push(`Enum ${enumName} {`);
    for (const value of values) {
      lines.push(`  ${value}`);
    }
    lines.push('}');
    lines.push('');
  }

  // Generate tables
  for (const table of schema.tables) {
    lines.push(generateDBMLTable(table));
    lines.push('');
  }

  // Generate refs (relationships)
  for (const rel of schema.relationships) {
    let refType = '>';  // many-to-one by default
    lines.push(`Ref: ${rel.source.table}.${rel.source.column} ${refType} ${rel.target.table}.${rel.target.column}`);
  }

  return lines.join('\n');
}

function generateDBMLTable(table: Table): string {
  const lines: string[] = [];

  lines.push(`Table ${table.name} {`);

  for (const column of table.columns) {
    let def = `  ${column.name} ${column.type}`;
    const settings: string[] = [];

    if (column.isPrimaryKey) settings.push('pk');
    if (!column.nullable) settings.push('not null');
    if (column.isUnique && !column.isPrimaryKey) settings.push('unique');
    if (column.defaultValue) settings.push(`default: ${column.defaultValue}`);
    if (column.references) {
      settings.push(`ref: > ${column.references.table}.${column.references.column}`);
    }

    if (settings.length > 0) {
      def += ` [${settings.join(', ')}]`;
    }

    lines.push(def);
  }

  lines.push('}');
  return lines.join('\n');
}

