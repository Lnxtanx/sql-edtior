// Markdown Documentation Generator
// =============================================================================
export function generateMarkdownDocs(schema: ParsedSchema): string {
  const lines: string[] = [];

  lines.push('# Database Schema Documentation');
  lines.push('');
  lines.push('*Generated by Schema Weaver*');
  lines.push('');
  lines.push('## Overview');
  lines.push('');
  lines.push(`- **Tables:** ${schema.tables.length}`);
  lines.push(`- **Relationships:** ${schema.relationships.length}`);
  lines.push(`- **Enums:** ${schema.enums.size}`);
  lines.push(`- **Views:** ${schema.views.length}`);
  lines.push(`- **Functions:** ${schema.functions.length}`);
  lines.push('');

  // Table of contents
  lines.push('## Table of Contents');
  lines.push('');
  for (const table of schema.tables) {
    lines.push(`- [${table.name}](#${table.name.toLowerCase()})`);
  }
  lines.push('');

  // Enums section
  if (schema.enums.size > 0) {
    lines.push('## Enums');
    lines.push('');
    for (const [enumName, values] of schema.enums.entries()) {
      lines.push(`### ${enumName}`);
      lines.push('');
      lines.push('| Value |');
      lines.push('|-------|');
      for (const value of values) {
        lines.push(`| \`${value}\` |`);
      }
      lines.push('');
    }
  }

  // Tables section
  lines.push('## Tables');
  lines.push('');

  for (const table of schema.tables) {
    lines.push(`### ${table.name}`);
    lines.push('');

    if (table.schema) lines.push(`**Schema:** ${table.schema}`);
    if (table.isPartitioned) lines.push(`**Partitioned:** Yes (${table.partitionType})`);
    if (table.isTemporary) lines.push(`**Temporary:** Yes`);
    if (table.inheritsFrom) lines.push(`**Inherits:** ${table.inheritsFrom}`);
    lines.push('');

    // Columns table
    lines.push('| Column | Type | Nullable | Key | Default |');
    lines.push('|--------|------|----------|-----|---------|');

    for (const column of table.columns) {
      const key = column.isPrimaryKey ? 'PK' : column.isForeignKey ? 'FK' : column.isUnique ? 'UQ' : '';
      const nullable = column.nullable ? 'Yes' : 'No';
      const defaultVal = column.defaultValue || '-';
      lines.push(`| \`${column.name}\` | \`${column.type}\` | ${nullable} | ${key} | ${defaultVal} |`);
    }
    lines.push('');

    // Foreign keys
    const fkColumns = table.columns.filter(c => c.references);
    if (fkColumns.length > 0) {
      lines.push('**Foreign Keys:**');
      lines.push('');
      for (const col of fkColumns) {
        let fkLine = `- \`${col.name}\` → \`${col.references!.table}.${col.references!.column}\``;
        if (col.references!.onDelete) fkLine += ` (ON DELETE ${col.references!.onDelete})`;
        lines.push(fkLine);
      }
      lines.push('');
    }
  }

  // Relationships diagram (text)
  lines.push('## Relationships');
  lines.push('');
  lines.push('```');
  lines.push('```');
  for (const rel of schema.relationships) {
    lines.push(`${rel.source.table}.${rel.source.column} ──> ${rel.target.table}.${rel.target.column}`);
  }
  lines.push('```');
  lines.push('');

  return lines.join('\n');
}

