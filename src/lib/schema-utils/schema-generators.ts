// Drizzle ORM Schema Generator
// =============================================================================
export function generateDrizzleSchema(schema: ParsedSchema): string {
  const lines: string[] = [];

  lines.push('// Drizzle ORM Schema');
  lines.push('// Generated by Schema Weaver');
  lines.push('');
  lines.push("import { pgTable, serial, text, varchar, integer, bigint, boolean, timestamp, uuid, json, jsonb, primaryKey, foreignKey, unique } from 'drizzle-orm/pg-core';");
  lines.push("import { relations } from 'drizzle-orm';");
  lines.push('');

  // Generate enums
  for (const [enumName, values] of schema.enums.entries()) {
    lines.push(`export const ${toCamelCase(enumName)} = ['${values.join("', '")}'] as const;`);
    lines.push(`export type ${toPascalCase(enumName)} = typeof ${toCamelCase(enumName)}[number];`);
    lines.push('');
  }

  // Generate tables
  for (const table of schema.tables) {
    lines.push(generateDrizzleTable(table));
    lines.push('');
  }

  // Generate relations
  for (const table of schema.tables) {
    const tableRels = schema.relationships.filter(r => r.source.table === table.name || r.target.table === table.name);
    if (tableRels.length > 0) {
      lines.push(generateDrizzleRelations(table, schema.relationships, schema.tables));
      lines.push('');
    }
  }

  return lines.join('\n');
}

function generateDrizzleTable(table: Table): string {
  const lines: string[] = [];
  const varName = toCamelCase(table.name);

  lines.push(`export const ${varName} = pgTable('${table.name}', {`);

  for (const column of table.columns) {
    lines.push(`  ${generateDrizzleColumn(column)},`);
  }

  lines.push('});');
  return lines.join('\n');
}

function generateDrizzleColumn(column: Column): string {
  const t = column.type.toLowerCase();
  let drizzleType: string;
  let modifiers: string[] = [];

  // Map PostgreSQL type to Drizzle
  if (t.includes('serial')) {
    drizzleType = t === 'bigserial' ? `bigserial('${column.name}', { mode: 'number' })` : `serial('${column.name}')`;
  } else if (t === 'uuid') {
    drizzleType = `uuid('${column.name}')`;
  } else if (t.includes('varchar')) {
    const match = t.match(/varchar\((\d+)\)/);
    drizzleType = match ? `varchar('${column.name}', { length: ${match[1]} })` : `varchar('${column.name}')`;
  } else if (t === 'text') {
    drizzleType = `text('${column.name}')`;
  } else if (t === 'integer' || t === 'int' || t === 'int4') {
    drizzleType = `integer('${column.name}')`;
  } else if (t === 'bigint' || t === 'int8') {
    drizzleType = `bigint('${column.name}', { mode: 'number' })`;
  } else if (t === 'boolean' || t === 'bool') {
    drizzleType = `boolean('${column.name}')`;
  } else if (t.includes('timestamp')) {
    drizzleType = `timestamp('${column.name}')`;
  } else if (t === 'date') {
    drizzleType = `date('${column.name}')`;
  } else if (t === 'json') {
    drizzleType = `json('${column.name}')`;
  } else if (t === 'jsonb') {
    drizzleType = `jsonb('${column.name}')`;
  } else {
    drizzleType = `text('${column.name}')`; // fallback
  }

  if (column.isPrimaryKey) {
    modifiers.push('.primaryKey()');
  }
  if (!column.nullable && !column.isPrimaryKey) {
    modifiers.push('.notNull()');
  }
  if (column.isUnique && !column.isPrimaryKey) {
    modifiers.push('.unique()');
  }
  if (column.defaultValue) {
    const def = mapDefaultToDrizzle(column.defaultValue, t);
    if (def) modifiers.push(def);
  }

  return `${column.name}: ${drizzleType}${modifiers.join('')}`;
}

function mapDefaultToDrizzle(defaultValue: string, type: string): string | null {
  const d = defaultValue.toLowerCase();
  if (d.includes('now()') || d.includes('current_timestamp')) return '.defaultNow()';
  if (d.includes('gen_random_uuid') || d.includes('uuid_generate')) return ".default(sql`gen_random_uuid()`)";
  if (d === 'true') return '.default(true)';
  if (d === 'false') return '.default(false)';
  if (!isNaN(Number(defaultValue))) return `.default(${defaultValue})`;
  if (defaultValue.startsWith("'") && defaultValue.endsWith("'")) return `.default(${defaultValue})`;
  return null;
}

function generateDrizzleRelations(table: Table, relationships: Relationship[], tables: Table[]): string {
  const lines: string[] = [];
  const varName = toCamelCase(table.name);

  lines.push(`export const ${varName}Relations = relations(${varName}, ({ one, many }) => ({`);

  // Outgoing relations (this table has FK)
  const outgoing = relationships.filter(r => r.source.table === table.name);
  for (const rel of outgoing) {
    const refVar = toCamelCase(rel.target.table);
    lines.push(`  ${refVar}: one(${refVar}, {`);
    lines.push(`    fields: [${varName}.${rel.source.column}],`);
    lines.push(`    references: [${refVar}.${rel.target.column}],`);
    lines.push('  }),');
  }

  // Incoming relations (other tables reference this)
  const incoming = relationships.filter(r => r.target.table === table.name);
  for (const rel of incoming) {
    const refVar = toCamelCase(rel.source.table);
    lines.push(`  ${refVar}s: many(${refVar}),`);
  }

  lines.push('}));');
  return lines.join('\n');
}

