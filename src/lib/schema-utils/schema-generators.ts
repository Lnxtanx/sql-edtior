// Schema Generators - Export to different formats
import { ParsedSchema, Table, Column, Relationship } from '../sql-parser';

// =============================================================================
// PostgreSQL DDL Generator (for bidirectional editing)
// =============================================================================
export function generatePostgresSQL(schema: ParsedSchema): string {
  const lines: string[] = [];

  // Generate ENUMs first
  for (const [enumName, values] of schema.enums.entries()) {
    lines.push(`CREATE TYPE ${enumName} AS ENUM (${values.map(v => `'${v}'`).join(', ')});`);
    lines.push('');
  }

  // Generate tables
  for (const table of schema.tables) {
    lines.push(generateTableSQL(table));
    lines.push('');
  }

  return lines.join('\n').trim();
}

export function generateTableSQL(table: Table): string {
  const lines: string[] = [];
  const tableName = table.schema && !table.name.startsWith(table.schema + '.')
    ? `${table.schema}.${table.name}`
    : table.name;

  let createPrefix = 'CREATE';
  if (table.isTemporary) createPrefix += ' TEMPORARY';
  if (table.isUnlogged) createPrefix += ' UNLOGGED';

  lines.push(`${createPrefix} TABLE ${tableName} (`);

  const columnDefs: string[] = [];
  const constraints: string[] = [];

  for (const column of table.columns) {
    columnDefs.push(`  ${generateColumnSQL(column)}`);
  }

  // Add table-level constraints
  for (const check of table.checkConstraints) {
    constraints.push(`  CONSTRAINT ${check} CHECK (...)`);
  }

  const allDefs = [...columnDefs, ...constraints];
  lines.push(allDefs.join(',\n'));

  let closing = ')';
  if (table.inheritsFrom) {
    closing += ` INHERITS (${table.inheritsFrom})`;
  }
  if (table.isPartitioned && table.partitionType) {
    closing += ` PARTITION BY ${table.partitionType.toUpperCase()} (...)`;
  }
  closing += ';';

  lines.push(closing);

  return lines.join('\n');
}

function generateColumnSQL(column: Column): string {
  let def = `${column.name} ${column.type}`;

  if (column.isPrimaryKey) {
    def += ' PRIMARY KEY';
  }

  if (!column.nullable && !column.isPrimaryKey) {
    def += ' NOT NULL';
  }

  if (column.isUnique && !column.isPrimaryKey) {
    def += ' UNIQUE';
  }

  if (column.defaultValue) {
    def += ` DEFAULT ${column.defaultValue}`;
  }

  if (column.references) {
    def += ` REFERENCES ${column.references.table}(${column.references.column})`;
    if (column.references.onDelete) {
      def += ` ON DELETE ${column.references.onDelete}`;
    }
    if (column.references.onUpdate) {
      def += ` ON UPDATE ${column.references.onUpdate}`;
    }
  }

  return def;
}

// =============================================================================
// Prisma Schema Generator
// =============================================================================
export function generatePrismaSchema(schema: ParsedSchema): string {
  const lines: string[] = [];

  lines.push('// This is your Prisma schema file');
  lines.push('// Generated by Schema Weaver');
  lines.push('');
  lines.push('generator client {');
  lines.push('  provider = "prisma-client-js"');
  lines.push('}');
  lines.push('');
  lines.push('datasource db {');
  lines.push('  provider = "postgresql"');
  lines.push('  url      = env("DATABASE_URL")');
  lines.push('}');
  lines.push('');

  // Generate enums
  for (const [enumName, values] of schema.enums.entries()) {
    lines.push(`enum ${toPascalCase(enumName)} {`);
    for (const value of values) {
      lines.push(`  ${value}`);
    }
    lines.push('}');
    lines.push('');
  }

  // Generate models
  for (const table of schema.tables) {
    lines.push(generatePrismaModel(table, schema));
    lines.push('');
  }

  return lines.join('\n');
}

function generatePrismaModel(table: Table, schema: ParsedSchema): string {
  const lines: string[] = [];
  const modelName = toPascalCase(table.name);

  lines.push(`model ${modelName} {`);

  for (const column of table.columns) {
    lines.push(`  ${generatePrismaField(column, table, schema)}`);
  }

  // Add reverse relations
  const reverseRels = schema.relationships.filter(r => r.target.table === table.name);
  for (const rel of reverseRels) {
    const relModel = toPascalCase(rel.source.table);
    lines.push(`  ${toCamelCase(rel.source.table)}s ${relModel}[]`);
  }

  // Add @@map if table name differs from model name
  if (table.name !== modelName.toLowerCase()) {
    lines.push('');
    lines.push(`  @@map("${table.name}")`);
  }

  lines.push('}');
  return lines.join('\n');
}

function generatePrismaField(column: Column, table: Table, schema: ParsedSchema): string {
  let prismaType = mapPostgresToPrisma(column.type, schema.enums);
  let modifiers: string[] = [];

  if (column.isPrimaryKey) {
    modifiers.push('@id');
    if (column.type.toLowerCase().includes('serial') || column.defaultValue?.includes('uuid')) {
      modifiers.push('@default(autoincrement())');
    }
    if (column.type.toLowerCase() === 'uuid') {
      modifiers.push('@default(uuid())');
    }
  }

  if (column.isUnique && !column.isPrimaryKey) {
    modifiers.push('@unique');
  }

  if (column.defaultValue) {
    const defaultVal = mapDefaultToPrisma(column.defaultValue);
    if (defaultVal) {
      modifiers.push(`@default(${defaultVal})`);
    }
  }

  if (column.references) {
    // This is a relation field
    const refModel = toPascalCase(column.references.table);
    lines.push(`  ${toCamelCase(column.references.table)} ${refModel}? @relation(fields: [${column.name}], references: [${column.references.column}])`);
  }

  let optional = column.nullable ? '?' : '';

  // Column name mapping if needed
  if (column.name.includes('_')) {
    const camelName = toCamelCase(column.name);
    if (camelName !== column.name) {
      modifiers.push(`@map("${column.name}")`);
    }
  }

  return `${column.name} ${prismaType}${optional} ${modifiers.join(' ')}`.trim();
}

function mapPostgresToPrisma(pgType: string, enums: Map<string, string[]>): string {
  const t = pgType.toLowerCase();

  // Check if it's an enum
  for (const enumName of enums.keys()) {
    if (t === enumName.toLowerCase()) {
      return toPascalCase(enumName);
    }
  }

  if (t.includes('serial') || t === 'integer' || t === 'int' || t === 'int4') return 'Int';
  if (t === 'bigint' || t === 'bigserial' || t === 'int8') return 'BigInt';
  if (t === 'smallint' || t === 'smallserial' || t === 'int2') return 'Int';
  if (t.includes('numeric') || t.includes('decimal')) return 'Decimal';
  if (t === 'real' || t === 'float4') return 'Float';
  if (t === 'double precision' || t === 'float8') return 'Float';
  if (t.includes('varchar') || t === 'text' || t.includes('char')) return 'String';
  if (t === 'boolean' || t === 'bool') return 'Boolean';
  if (t.includes('timestamp') || t === 'date') return 'DateTime';
  if (t === 'time') return 'DateTime';
  if (t === 'uuid') return 'String';
  if (t === 'json' || t === 'jsonb') return 'Json';
  if (t === 'bytea') return 'Bytes';
  if (t.endsWith('[]')) return mapPostgresToPrisma(t.slice(0, -2), enums) + '[]';

  return 'String';
}

function mapDefaultToPrisma(defaultValue: string): string | null {
  const d = defaultValue.toLowerCase();
  if (d.includes('now()') || d.includes('current_timestamp')) return 'now()';
  if (d === 'true') return 'true';
  if (d === 'false') return 'false';
  if (d.includes('gen_random_uuid') || d.includes('uuid_generate')) return 'uuid()';
  if (d.includes('autoincrement') || d.includes('nextval')) return 'autoincrement()';
  return null;
}

