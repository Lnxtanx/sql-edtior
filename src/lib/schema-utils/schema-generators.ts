// TypeScript Types Generator
// =============================================================================
export function generateTypeScriptTypes(schema: ParsedSchema): string {
  const lines: string[] = [];

  lines.push('// TypeScript Types');
  lines.push('// Generated by Schema Weaver');
  lines.push('');

  // Generate enum types
  for (const [enumName, values] of schema.enums.entries()) {
    lines.push(`export type ${toPascalCase(enumName)} = ${values.map(v => `'${v}'`).join(' | ')};`);
    lines.push('');
  }

  // Generate table interfaces
  for (const table of schema.tables) {
    lines.push(`export interface ${toPascalCase(table.name)} {`);
    for (const column of table.columns) {
      const tsType = mapPostgresToTypeScript(column.type, schema.enums);
      const optional = column.nullable ? '?' : '';
      lines.push(`  ${column.name}${optional}: ${tsType};`);
    }
    lines.push('}');
    lines.push('');
  }

  return lines.join('\n');
}

function mapPostgresToTypeScript(pgType: string, enums: Map<string, string[]>): string {
  const t = pgType.toLowerCase();

  // Check if it's an enum
  for (const enumName of enums.keys()) {
    if (t === enumName.toLowerCase()) {
      return toPascalCase(enumName);
    }
  }

  if (t.includes('int') || t.includes('serial') || t.includes('numeric') || t.includes('decimal') || t === 'real' || t.includes('float') || t.includes('double')) return 'number';
  if (t.includes('varchar') || t === 'text' || t.includes('char') || t === 'uuid') return 'string';
  if (t === 'boolean' || t === 'bool') return 'boolean';
  if (t.includes('timestamp') || t === 'date' || t === 'time') return 'Date';
  if (t === 'json' || t === 'jsonb') return 'Record<string, unknown>';
  if (t === 'bytea') return 'Buffer';
  if (t.endsWith('[]')) return mapPostgresToTypeScript(t.slice(0, -2), enums) + '[]';

  return 'unknown';
}

// =============================================================================
// Helpers
// =============================================================================
function toPascalCase(str: string): string {
  return str
    .replace(/[-_](.)/g, (_, c) => c.toUpperCase())
    .replace(/^(.)/, (_, c) => c.toUpperCase());
}

function toCamelCase(str: string): string {
  return str
    .replace(/[-_](.)/g, (_, c) => c.toUpperCase())
    .replace(/^(.)/, (_, c) => c.toLowerCase());
}

